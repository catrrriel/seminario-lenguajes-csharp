// Investigar secuencias de escape.

// \\ → Barra invertida (\)
// \" → Comilla doble (")
// \' → Comilla simple (')
// \n → Nueva línea
// \r → Retorno de carro
// \t → Tabulación horizontal
// \b → Retroceso (backspace)
// \f → Alimentación de formulario (form feed)
// \a → Alerta (sonido)
// \v → Tabulación vertical
// \0 → Carácter nulo (Unicode 0x0000) 

Console.Write("hola mi nombre es \"catriel\"\n");
Console.Write("hola mi nombre es \\ catriel");
